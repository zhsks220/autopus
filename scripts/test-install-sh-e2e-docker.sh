#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
source "$ROOT_DIR/scripts/lib/docker-build.sh"
IMAGE_NAME="${AUTOPUS_INSTALL_E2E_IMAGE:-autopus-install-e2e:local}"
INSTALL_URL="${AUTOPUS_INSTALL_URL:-https://autopus.bot/install.sh}"

OPENAI_API_KEY="${OPENAI_API_KEY:-}"
ANTHROPIC_API_KEY="${ANTHROPIC_API_KEY:-}"
ANTHROPIC_API_TOKEN="${ANTHROPIC_API_TOKEN:-}"
AUTOPUS_E2E_MODELS="${AUTOPUS_E2E_MODELS:-}"

echo "==> Build image: $IMAGE_NAME"
docker_build_run install-e2e-build \
  -t "$IMAGE_NAME" \
  -f "$ROOT_DIR/scripts/docker/install-sh-e2e/Dockerfile" \
  "$ROOT_DIR/scripts/docker"

echo "==> Run E2E installer test"
docker run --rm \
  -e AUTOPUS_INSTALL_URL="$INSTALL_URL" \
  -e AUTOPUS_INSTALL_TAG="${AUTOPUS_INSTALL_TAG:-latest}" \
  -e AUTOPUS_E2E_MODELS="$AUTOPUS_E2E_MODELS" \
  -e AUTOPUS_INSTALL_E2E_OPENAI_MODEL="${AUTOPUS_INSTALL_E2E_OPENAI_MODEL:-}" \
  -e AUTOPUS_INSTALL_E2E_OPENAI_PROVIDER_TIMEOUT_SECONDS="${AUTOPUS_INSTALL_E2E_OPENAI_PROVIDER_TIMEOUT_SECONDS:-}" \
  -e AUTOPUS_INSTALL_E2E_PREVIOUS="${AUTOPUS_INSTALL_E2E_PREVIOUS:-}" \
  -e AUTOPUS_INSTALL_E2E_SKIP_PREVIOUS="${AUTOPUS_INSTALL_E2E_SKIP_PREVIOUS:-0}" \
  -e AUTOPUS_INSTALL_E2E_AGENT_TURN_TIMEOUT_SECONDS="${AUTOPUS_INSTALL_E2E_AGENT_TURN_TIMEOUT_SECONDS:-300}" \
  -e AUTOPUS_INSTALL_E2E_AGENT_TURNS_PARALLEL="${AUTOPUS_INSTALL_E2E_AGENT_TURNS_PARALLEL:-1}" \
  -e AUTOPUS_INSTALL_E2E_AGENT_TOOL_SMOKE="${AUTOPUS_INSTALL_E2E_AGENT_TOOL_SMOKE:-1}" \
  -e AUTOPUS_NO_ONBOARD=1 \
  -e OPENAI_API_KEY \
  -e ANTHROPIC_API_KEY \
  -e ANTHROPIC_API_TOKEN \
  "$IMAGE_NAME"
